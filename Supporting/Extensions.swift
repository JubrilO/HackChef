import Foundation
import UIKit

extension UIColor {
    class var hcLightGrey: UIColor {
        return UIColor(red: 0.9, green: 0.9, blue: 0.9, alpha: 1)
    }
    class var hcHighlightColor: UIColor {
        return UIColor(red: 1, green: 0.94, blue: 0.92, alpha: 1)
    }
}

